static afxResult DecodeUtf8(afxBool* reject, unsigned int* codep, afxNat byte)
{
    /*
        Exemplo:

        codepoint = 0;

        if (DecodeUtf8(&state, &codepoint, *(unsigned char*)s))
            continue;
    */

    afxNat type = utf8d[byte];
    *codep = (*reject) ? (byte & 0x3fu) | (*codep << 6) : (0xff >> type) & (byte);
    *reject = utf8d[256 + *reject * 16 + type];
    return *reject;
}

